    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jTable1.getSelectedRow() >= 0) {
            switch (jTable1.getToolTipText()) {
                case "formulaMedica":
                    this.dispose();
                    instancias.getFormulaMedicaOf().reimprimir(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    break;
                case "formLentes":
                    this.dispose();
                    String id = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                    instancias.getFormulaLentes().reimprimir(id);
                    break;
                case "ayudaDiagnostica":
                    this.dispose();
                    instancias.getAyudaDiagnosticoOf().reimprimir(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    break;
                case "incapacidad":
                    this.dispose();
                    instancias.getIncapacidadOf().reimprimir(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    break;
                case "biometria":
                    this.dispose();
                    instancias.getBiometria().reimprimir(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    break;
                case "paquimetria":
                    this.dispose();
                    instancias.getPaquimetria().reimprimir(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    break;
            }
        } else {
            this.dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

