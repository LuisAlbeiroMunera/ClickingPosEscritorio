    public void cargarRegistros(String paciente, String examen, Instancias instancias, String hist) {

        this.instancias = instancias;

        if (nombre != null) {
            jTextField2.setText(nombre);
        } else {
            jTextField2.setText(instancias.getSql().getDatosTercero(paciente).getNombre());
        }

        Object[][] datos = null;

        switch (examen) {
            case "formulaMedica":
                datos = instancias.getSql().getRegistrosExamens3(hist, "formulaMedicaOf");
                break;
            case "ayudaDiagnostica":
                modelo = new DefaultTableModel(datos, new Object[]{"Id", "Fecha", "Concepto"});
                jTable1.setModel(modelo);
                this.show();
                jTable1.setToolTipText(examen);
                datos = instancias.getSql().getRegistrosAyuda(hist, "conAyudaDiagnosticaOf");
                break;
            case "formLentes":
                datos = instancias.getSql().getRegistrosFormulaLentes(paciente, "formulaLentes");
                break;
            case "incapacidad":
                modelo = new DefaultTableModel(datos, new Object[]{"Id", "Fecha Inicio", "Fecha Vencimiento", "Descripcion"});
                jTable1.setModel(modelo);
                this.show();
                jTable1.setToolTipText(examen);
                datos = instancias.getSql().getRegistrosIncapacidad(hist, "bdIncapacidadOf");
                break;
            case "biometria":
                modelo = new DefaultTableModel(datos, new Object[]{"Id", "Fecha", "Cedula"});
                jTable1.setModel(modelo);
                this.show();
                jTable1.setToolTipText(examen);
                datos = instancias.getSql().getRegistrosBiometria(paciente, "bdBiometria");
                break;
            case "paquimetria":
                datos = instancias.getSql().getRegistrosPaquimetria(paciente, "bdPaquimetria");
                break;
        }

        for (Object[] dato : datos) {
            modelo.addRow(dato);
        }

        jTable1.setToolTipText(examen);
    }

