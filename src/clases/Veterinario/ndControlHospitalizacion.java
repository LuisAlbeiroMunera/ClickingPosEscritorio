package clases.Veterinario;

public class ndControlHospitalizacion {
    private String Id, idHospitalizacion, fecha, hora, usuario;

    public String getId() {
        return Id;
    }

    public void setId(String Id) {
        this.Id = Id;
    }

    public String getIdHospitalizacion() {
        return idHospitalizacion;
    }

    public void setIdHospitalizacion(String idHospitalizacion) {
        this.idHospitalizacion = idHospitalizacion;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
}
