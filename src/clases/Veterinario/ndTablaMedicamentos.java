package clases.Veterinario;

public class ndTablaMedicamentos {

    String idHospitalizacion, medicamentos, codigo, medicamento, posologia, hora;

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getIdHospitalizacion() {
        return idHospitalizacion;
    }

    public void setIdHospitalizacion(String idHospitalizacion) {
        this.idHospitalizacion = idHospitalizacion;
    }

    public String getMedicamentos() {
        return medicamentos;
    }

    public void setMedicamentos(String medicamentos) {
        this.medicamentos = medicamentos;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getMedicamento() {
        return medicamento;
    }

    public void setMedicamento(String medicamento) {
        this.medicamento = medicamento;
    }

    public String getPosologia() {
        return posologia;
    }

    public void setPosologia(String posologia) {
        this.posologia = posologia;
    }
}
